package myProject;

public class variable_03 {
	
	public static void main(String args[]) {
		
		// six types of variabe in java  and one class -> (String) 
		
		int a= 10; // integer variable
		System.out.println("a is "+ a);
		System.out.println(" a+a is "+ a+a); // output :-> 1010
		System.out.println("a+a is "+ (a+a));// output :-> 20
		System.out.println(a +" "+ (a+a));   // output :-> 10 20
		
		long l= 108464648; // long integer variable
		System.out.println("long value l is "+ l);
		
		float b= 10.67f; // floating point variable
		System.out.println("flaoting point b is "+ b);
		
		double d= 10.6793736; // double floating point variable
		System.out.println("Double point d is "+ d);
		
		char c = 'A'; // charactor  variable
		System.out.println("charactor value c is "+ c);
		
		boolean bb  = true; // String  variable
		System.out.println("boolean value bb is "+ bb);
		
		String s  = "Anuj kumar"; // String  variable
		System.out.println("String value s is "+ s);
		
		
		
	}

	
}
