package myProject;

public class operator_05 {

	public static void main(String args[]) {
		
		System.out.println(10 + 10);
		System.out.println(10 > 10);
		System.out.println(10 != 40);
		System.out.println(10 > 5 ? 100 : 400); // ternary operators
		System.out.println(10 == 20);
		System.out.println(12 > 10 && 15 != 15);
		System.out.println(!(17>20));
		System.out.println(90>28 && 59!= 77 );
		System.out.println(100 >230 || 90 != 121);
		System.out.println(100 >230 || 90 != 121);
	}
}
